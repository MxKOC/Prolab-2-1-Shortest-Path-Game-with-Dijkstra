
package prolab4;



public class Obje {
    private int değer;
    private int süre;

    public Obje(int değer, int süre) {
        this.değer = değer;
        this.süre = süre;

    }

    public Obje() {
    }

    
    
    
    public int getDeğer() {
        return değer;
    }

    public void setDeğer(int değer) {
        this.değer = değer;
    }

    public int getSüre() {
        return süre;
    }

    public void setSüre(int süre) {
        this.süre = süre;
    }

}
